    private void btnAgregarPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPlatoActionPerformed

        //ASIGNA EL LISTADO DE BOTONES DE MESAS A UNA LISTA
        List<JToggleButton> botonesMesas = agregarBtn();
        
        //ASIGNA EL LISTADO DE CHECKBOX DE PRODUCTOS A UNA LISTA
        List<JCheckBox> cbxProductos = agregarCbx();
        
        //ITERA PARA CONOCER LA MESA SELECCIONADA
        if(clientes.isEmpty()){
            for (JToggleButton bMesa : botonesMesas) {
                if (bMesa.isSelected()) {

                    //SE UTILIZA EL METODO "mesaEnArray()" PARA COMPROBAR LA EXISTENCIA DE LA MESA Y LUEGO
                    //UTILIZARLA PARA AGREGAR LOS NUEVOS PRODUCTOS
                    if (mesaEnArray(Integer.parseInt(bMesa.getText()))) {
                        Registro rg = registroEnArray(Integer.parseInt(bMesa.getText()));
                        registro.remove(rg);
                        for (JCheckBox cbxProducto : cbxProductos) {
                            if (cbxProducto.isSelected()) {
                                rg.getProductos().add(obtenerProductoSeleccionado(cbxProducto));
                            }
                        }

                        //IMPRIME EN "txaPedido" LOS PRODUCTOS AGREGADOS
                        txaPedido.setText("====== Mesa " + bMesa.getText() + " ======\n\n");
                        for (Producto producto : rg.getProductos()) {
                            txaPedido.append(producto.getTipo() + ": " + producto.getNombre() + "\n");
                        }
                        registro.add(rg);

                        //INSTANCIA UN NUEVO OBJETO "Registro()" PARA AGREGAR LOS DATOS DE SUS
                        //VARIABLES (MESA Y PRODUCTOS)
                    } else {
                        Registro rg = new Registro();
                        Mesa mesaSeleccionada = new Mesa(Integer.parseInt(bMesa.getText()), true);
                        rg.setMesa(mesaSeleccionada);
                        for (JCheckBox cbxProducto : cbxProductos) {
                            if (cbxProducto.isSelected()) {
                                rg.getProductos().add(obtenerProductoSeleccionado(cbxProducto));
                            }
                        }
                        registro.add(rg);

                        //IMPRIME EN "txaPedido" LOS PRODUCTOS AGREGADOS
                        txaPedido.setText("====== Mesa " + bMesa.getText() + " ======\n\n");
                        for (Producto producto : rg.getProductos()) {
                            txaPedido.append(producto.getTipo() + ": " + producto.getNombre() + "\n");
                        }
                    }
                }
            }
        }else{
            txaPedido.setText("======= "+clientes.get(0).getNombre()+" =======\n\n");
                Registro rg = registroEnArrayCliente(clientes.get(0).getNombre());
                registro.remove(rg);
                rg.setCliente(clientes.get(0));

                    for (JCheckBox cbxProducto : cbxProductos) {
                        if (cbxProducto.isSelected()) {
                            rg.getProductos().add(obtenerProductoSeleccionado(cbxProducto));
                        }
                    }

                    for (Producto producto : rg.getProductos()) {
                        txaPedido.append(producto.getTipo() + ": " + producto.getNombre() + "\n");
                    }
                registro.add(rg);
        }
    }//GEN-LAST:event_btnAgregarPlatoActionPerformed
    
    

    //RETORNA LA MESA Y SU LISTADO DE PRODUCTOS ASOCIADOS
    private Registro registroEnArray(int numeroMesa){
        Registro rg = new Registro();
        for (Registro rgTemp : registro) {
            if (rgTemp.getMesa().getNumeroMesa() == numeroMesa){
                rg = rgTemp;
            }
        }
        return rg;
    }
    
    private Registro registroEnArrayCliente(String nombreCliente){
        Registro rg = new Registro();
        for (Registro rgTemp : registro) {
            if (rgTemp.getCliente().getNombre().equalsIgnoreCase(nombreCliente)){
                rg = rgTemp;
            }
        }
        return rg;
    }
    
    //COMPRUEBA SI LA MESA EXISTE DENTRO DEL LISTADO DE MESAS
    private boolean mesaEnArray(int numeroMesa){
        
        boolean existe = false;
        for (Registro rgTemp : registro) {
            if (rgTemp.getMesa().getNumeroMesa() == numeroMesa){
                existe = true;
            }
        }
        return existe;
    }
    
